inline const uint8_t MPL3115_FieldInfo::GetNumBits() const {
  return msbIndex - lsbIndex + 1;
}

inline bool MPL3115_FieldInfo::operator==(
    const MPL3115_FieldInfo &other) const {
  if (other.lsbIndex == lsbIndex && other.msbIndex == msbIndex &&
      other.reg == reg) {
    return true;
  }
  return false;
}

