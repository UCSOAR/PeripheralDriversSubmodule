typedef uint8_t BIT_INDEX;

class MPLSENSDriver;

struct MPL3115_FieldInfo {
  MPL3115_REGISTER_t reg;
  BIT_INDEX msbIndex;
  BIT_INDEX lsbIndex;
  const uint8_t GetNumBits() const;
  const bool writeable;
  bool operator==(const MPL3115_FieldInfo &other) const;
};

// Base class for a bit field within an MPL3115 register.
// Derived classes just overload the virtual functions to return their own
// constants.
class MPLSENSField {
 public:
  uint8_t GetNumBits();
  bool operator==(const MPLSENSField &other);
  const MPL3115_FieldInfo Info() const;
  virtual const BIT_INDEX getLsbIndex() const { return 0; }
  virtual const BIT_INDEX getMsbIndex() const { return 0; }
  virtual const MPL3115_REGISTER_t getReg() const {
    return MPL3115_REGISTER_t::WHO_AM_I;
  }
  operator MPL3115_FieldInfo() const { return Info(); }

  virtual bool writeable() const { return true; }
};

